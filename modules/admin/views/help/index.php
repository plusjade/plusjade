	<div id="album_help" class="help_box">
		<h2>Album Tool</h2>
		
		<p><strong>Usage</strong></p>
		<div class="indent">
			The album tool manages and displays image albums on your website.
			<br>You can create and arrange photo albums and specify where and how
			you want to display the album on your website.
		</div>
		
		<p><strong>Creating an Album</strong></p>
		<div class="indent">
			Add the album tool to the page you want it displayed on.
			"Add images" will bring up your files browser. 
			Drag and drop images from your files browser into the gray album space.
			
			Each album does <em>not</em> store the actual image, rather the album simply
			references the images you specify.
			
			<br>This means you can have duplicate images within the same album, or across
			multiple albums. However, deleting or renaming the image itself, will break
			the reference.
		</div>

		<p><strong>Editing an Album</strong></p>
		<div class="indent">
			You can delete image references by selecting the images to be deleted
			and clicking "delete selected.  Arrange the image order by dragging and 
			dropping the images into their new positions.
		</div>

		<p><strong>Selecting Album Display Types</strong></p>
		<div class="indent">
			Albums can be displayed in various ways:
			
			<ul>
				<li>Lightbox View (default)</li>
				<li>Gallery View</li>
				<li>Slideshow View</li>
			</ul>
		</div>
		
