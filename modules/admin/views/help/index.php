	<div id="content_help" class="help_box">
		<h2>Working with Content</h2>
		<div class="indent">
			+Jade uses <span style="background-color: rgb(255, 255, 153);"><strong>Tools</strong></span>
			to help you add and manage content easily, and efficiently.&nbsp; 
			Since all content is created, and managed via tools in +Jade, please remember that any mention of &quot;<strong>Tool</strong>&quot; is synonymous with &quot;content&quot; in this regard.
		</div>

		<p><strong>Adding Content</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/add_content.gif" alt=""/>
			Add content to a page by clicking the &quot;Add Content&quot; link on the Top Admin Bar.&nbsp;
			<br>Choose a tool that adds the type of content you wish to add to the page.
			<br>The tool will automatically take you to a new panel allowing you to add your content.	
		</div>

		<p><strong>Editing Content</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/red_toolbar_edit.gif" alt=""/>
			Edit content on your page by clicking the &quot;edit&quot; link 
			located on the right most side of the Red Toolbar directly above the content you wish to edit. A dropdown will appear showing various editing options associated with the particular content tool.
		</div>

		<p><strong>Moving Content.</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/red_toolbar_move.gif" alt=""/>
			Move content tools by placing your cursor on the leftmost side of the Red Toolbar associated with the content tool you wish to move. The left side of the toolbar contains the tool name and a blue &quot;move&quot; icon. Hovering over the icon should change its color to black. Click + hold to drag the content tool to a new position. The positons of the tools will automatically save when done.
		</div>

		<p><strong>Deleting Content.</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/tool_dropdown.gif" style="float:right;margin-left:5px;" alt=""/>
			The Red Toolbar associated with the content tool you wish to delete contains a &quot;delete action&quot;.
			Click the link, then confirm that you wish to delete this tool. Note that deleting tools cannot be undone.
		</div>
	</div>


	<div id="page_help" class="help_box">
		<h2>Working with Pages</h2>
		
		<div class="indent">
			<img src="/_assets/images/admin/help/page_browser.jpg" alt=""/>
			Pages in +Jade work similar to your computer folder system.
			<br>You can create pages and sub pages by assigning pages to work as "directories".
		</div>
		
		<p><strong>Adding a Page</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/pages_tab.gif" alt=""/>
			<img src="/_assets/images/admin/page_add.gif" style="float:right;margin-left:5px;" alt=""/>
			To add a new page to your website, click on the &quot;Pages&quot; tab in the Top Admin Bar.
			<br>Drag the &quot;add page&quot; icon into the directory you wish to add a page to.
			<br>A dialog allowing you to set the page name, url and page template will appear.
			Save this and your page is added.</p>
		</div>
		
		<p><strong>Editing a Page</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/settings_tab.gif" alt=""/>
			Edit page settings by navigating to the page you wish to edit. On the Top Admin Bar click the tab labled &quot;Settings&quot;.
			You can edit page visibility, name, url, title, description and template.		
			<br>Clicking on the page icons while in the page browser also reveals the "settings" action.
			(see pic below).
		
		</div>
		
		<p><strong>Deleting a Page</strong></p>
		<div class="indent">
			<img src="/_assets/images/admin/help/page_actions.gif" style="float:right;margin-left:5px;" alt=""/>
			Pages can only be deleted from from the Page Browser.
			Open the page browser by clicking &quot;Pages&quot; on the Top Admin Bar.
			Clicking on the page icons will reveal the &quot;delete&quot; action for the selected page.
			Note that deleting a page does NOT delete the tools contained on the page.
		</div>
	</div>

	
	
	<div id="menu_help" class="help_box">
		<h2>Working with the Primary Navigation Menu</h2>
		
		<div class="indent">
		Your primary navigation menu is how your visitors will likely get around your site.
		It is important to make clear, organized navigational links to your pages.
		Each new page automatically gets added to the primary navigation.
		</div>
		
		<p><strong>Editing Your Menu</strong></p>
		<div class="indent">
			To edit your menu, click on the tab marked "Site" on the Top Admin Bar.
			In the dropdown menu select "Navigation".
			In the menu view, you can drag pages around to rearrange the menu order.
		</div>

		<p><strong>Adding Menu items.</strong></p>
		<div class="indent">
			All new pages you create are automatically added to the main menu unless you select otherwise.
			To add pages or links to the menu, choose the appropriate action on the left hand side of the
			menu. Add internal pages using the "add page" selection dropdown. External links are added via
			"add external link" input field.
		</div>
		
		<p><strong>Removing Menu items.</strong></p>
		<div class="indent">
			Click the "hide" action to the right of the item.
			Confirm your action and the menu item no longer displays on the menu.
		</div>
	</div>


